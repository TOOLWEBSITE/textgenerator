    // Generate fancy text
    function generateFancyText() {
        const text = inputText.value;
        const style = fontStyle.value;
        
        if (!text.trim()) {
            outputText.textContent = 'Please enter some text first!';
            previewText.textContent = 'Your fancy text will appear here...';
            charCount.textContent = '0';
            copyBtn.disabled = true;
            return;
        }
        
        let result = '';
        const mapping = fontMappings[style];
        
        for (let i = 0; i < text.length; i++) {
            const char = text[i];
            const lowerChar = char.toLowerCase();
            
            if (mapping[lowerChar]) {
                // Handle case (if the mapping has uppercase versions)
                if (char === char.toUpperCase() && char !== char.toLowerCase() && 
                    mapping[char.toLowerCase()] !== mapping[char]) {
                    result += mapping[char.toLowerCase()].toUpperCase();
                } else {
                    result += mapping[lowerChar];
                }
            } else {
                // For characters not in our mapping (like punctuation), use the original
                result += char;
            }
        }
        
        outputText.textContent = result;
        previewText.textContent = result;
        charCount.textContent = result.length;
        copyBtn.disabled = false;
    }
    
    // Copy to clipboard
    function copyToClipboard() {
        const text = outputText.textContent;
        
        navigator.clipboard.writeText(text).then(() => {
            const originalText = copyBtn.textContent;
            copyBtn.textContent = 'Copied!';
            copyBtn.style.backgroundColor = '#218838';
            
            setTimeout(() => {
                copyBtn.textContent = originalText;
                copyBtn.style.backgroundColor = '';
            }, 2000);
        }).catch(err => {
            console.error('Failed to copy text: ', err);
            alert('Failed to copy text. Please try again or copy manually.');
        });
    }
    
    // Clear all fields
    function clearAll() {
        inputText.value = '';
        outputText.textContent = '';
        previewText.textContent = 'Your fancy text will appear here...';
        charCount.textContent = '0';
        fontStyle.value = 'normal';
        copyBtn.disabled = true;
        inputText.focus();
    }
    
    // Event listeners
    generateBtn.addEventListener('click', generateFancyText);
    copyBtn.addEventListener('click', copyToClipboard);
    clearBtn.addEventListener('click', clearAll);
    
    // Generate on Enter key in textarea
    inputText.addEventListener('keydown', function(e) {
        if (e.key === 'Enter' && !e.shiftKey) {
            e.preventDefault();
            generateFancyText();
        }
    });
    
    // Generate when style changes (if there's text)
    fontStyle.addEventListener('change', function() {
        if (inputText.value.trim()) {
            generateFancyText();
        }
    });
    
    // Initialize
    clearAll();
